package Basics;

public class TypeCasting {
    public static void main(String[] args) {
        float a = 12.34f;
        //type casting
        int b = (int)a;
        System.out.println(b);
        char ch = 'a';
        //type conversion
        int num = ch;
        System.out.println(num); //ascii value
    }
}
